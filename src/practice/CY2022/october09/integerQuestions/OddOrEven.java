package practice.CY2022.october09.integerQuestions;

public class OddOrEven {

    public static boolean oddOrEven(int element)
    {
        if(element == 0 || element == 0)
        {
            return false;
        }
        else if(element %2 ==0)
        {
            return true;
        }
        else
        {
            return false;
        }
    }
}
